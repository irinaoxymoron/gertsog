				<h3 class="article__title">Как работают вебкам модели в видеочате</h3>
				<p class="article__text">Работа интернет модели заключается в общении посредством веб камеры. Успех во многом зависит от умения построить интересный диалог с собеседником, найти общий язык с человеком и заинтересовать его снова и снова возвращаться в приватный чат, чтобы продолжить общение. Но как и в реальной жизни, в вебкам чате встречают, что называется, по одежке. Поэтому важно не только быть интересным собеседником, но и грамотно позировать, чтобы продемонстрировать на камеру все свои самые лучшие черты и скрыть возможные недостатки.</p>
				<p class="article__text">Умение позировать на камеру в видеочате, как и многие другие навыки вебкам моделей, приходит с опытом, но моделям, которые только начинают работать в этой сфере, стоит знать основные правила поведения перед камерой, чтобы быстрее достичь успеха в этом деле.</p>
				<h3 class="article__title">Уверенность в себе</h3>
				<p class="article__text">Камера всегда очень точно передает настроение, в котором девушки позируют перед вебкой. И первое, что выдает неуверенность в себе — это осанка. Сутулые плечи, зажатые позы или натянутая улыбка выглядят напряженно. А посетители чата хотят провести время максимально приятно и расслабленно, поэтому им не интересно общаться с моделью, которая сама чувствует себя некомфортно, ведь это состояние очень легко передается и собеседнику.</p>	
				<p class="article__text">Так что первое, о чем следует помнить при позировании перед вебкой, это осанка. Распрямите плечи, найдите удобную и естественную позу. Не бойтесь менять положение перед камерой, ведь в жизни мы не находимся постоянно в статичном положении. Для этого представьте, что вы общаетесь с человеком не онлайн, а просто принимаете его у себя в гостях. А приватном чате вы — хозяйка, а посетитель ваш гость. Покажите ему, что вы рады общению и получаете от этого настоящее удовольствие. Расслабиться и нащупать общие темы для разговора помогут легкие наводящие вопросы и шутки. Приходите в чат в хорошем настроении, с улыбкой и желанием сделать приятное вашему собеседнику. Тогда и он, почувствовав ваше расположение, захочет получше узнать вас и продолжить общение.</p>

				<h3 class="article__title">Самые эффектные позы для веб камеры</h3>
				<p class="article__text">Опытные девушки знают, какие позы превращают видеочат вебкам модели в по-настоящему популярный, и охотно делятся своими наработками с новичками.</p>	

				<p class="article__text">Сидя лицом к камере.</p>
				<p class="article__text">Здесь как раз и важна осанка, о которой мы говорили выше. Держите спину прямо. Если вам нужно наклониться вперед, то делайте это, опираясь на бедра, а не сгибая спину. В такой позе собеседнику хорошо видна ваша грудь, талия и лицо. Это лучшая поза для начала общения, когда важно хорошенько рассмотреть друг друга. Если вы считаете, что анфас — не самый выгодный для вас ракурс, можно сесть боком. Не забываем про осанку!</p>

				<p class="article__text">Лежа.</p>
				<p class="article__text">Такая поза может быть расслабленной, игривой и даже весьма эротичной. Смотря какой эффект вы хотите произвести, можно выбрать один из вариантов. Лежа на боку, девушка позирует перед вебкой, опираясь на согнутый локоть, немного согните ту ногу, что сверху, а нижнюю выпрямите. Такая поза максимально подчеркнет вашу стройность, визуально удлинит ноги.</p>

				<p class="article__text">Полусидя.</p>
				<p class="article__text">Еще один эффектный вариант: опереться на слегка согнутые и немного заведенные за спину руки, а ноги подогнуть вбок. Если вы предпочитаете, чтобы собеседник видел ваш профиль, то сядьте боком к камере, обопритесь на руки за спиной и выпрямите ноги, одну можно согнуть в колене.</p>		

				<p class="article__text">Портрет.</p>
				<p class="article__text">Это простая поза не требует каких-либо усилий и демонстрирует собеседнику ваше лицо крупным планом или лицо и плечи. В этом случае важно уделить внимание макияжу (его не должно быть слишком много, но он должен при этом хорошо скрывать изъяны кожи, если они имеются) и освещению. Следите, чтобы на лицо не падали тени.</p>		


